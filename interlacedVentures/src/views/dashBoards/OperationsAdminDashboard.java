    private void btnStorageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStorageActionPerformed
        // TODO add your handling code here:
        this.hide();
        StorageDash sd = new StorageDash();
        
        String[][] rows = new String[StorageDirectory.getInstance().getStorageDir().size()][5];
        String[] columnNames = {"StorageId", "Type", "Price", "availability", "Rented By"};
        int j = 0;
        for(int i=0; i < StorageDirectory.getInstance().getStorageDir().size(); i++){
                rows[j][0] = Integer.toString(StorageDirectory.getInstance().getStorageDir().get(i).getStorageID());
                rows[j][1] = StorageDirectory.getInstance().getStorageDir().get(i).getType();
                rows[j][2] = Float.toString((StorageDirectory.getInstance().getStorageDir().get(i).getPrice()));
                rows[j][3] = Boolean.toString(StorageDirectory.getInstance().getStorageDir().get(i).isAvailability());           
                rows[j][4] = StorageDirectory.getInstance().getStorageDir().get(i).getRentedBy();
                j++;
            
        }
        DefaultTableModel model = new DefaultTableModel (rows, columnNames);
        sd.tblStorage.setModel(model);
        
        sd.show();
    }//GEN-LAST:event_btnStorageActionPerformed

    private void btnServiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnServiceActionPerformed
        // TODO add your handling code here:
        this.hide();
        ViewItemsOperation vio = new ViewItemsOperation();
        vio.lblReqOp.setText("Services");
        String[] columnNames = {"Order From", "Organisation", "Service Requested", "Status", "ItemId", "Requested By", "OrderID"};
        int n = OrdersDirectory.getInstance().getOrdersDir().size() + RentalOrderDirectory.getInstance().getRentalOrdersDir().size();
        String[][] rows = new String[n][7];
        int j=0;
        
        for(int i = 0; i < RentalOrderDirectory.getInstance().getRentalOrdersDir().size(); i++){
            if(!RentalOrderDirectory.getInstance().getRentalOrdersDir().get(i).getStatus().equals("Completed") && RentalOrderDirectory.getInstance().getRentalOrdersDir().get(i).getRequestTo().equals("Operations")){
                rows[j][0] = RentalOrderDirectory.getInstance().getRentalOrdersDir().get(i).getOrderedBy();
                rows[j][1] = RentalOrderDirectory.getInstance().getRentalOrdersDir().get(i).getRequestTo();
                rows[j][2] = RentalOrderDirectory.getInstance().getRentalOrdersDir().get(i).getItem();
                rows[j][3] = RentalOrderDirectory.getInstance().getRentalOrdersDir().get(i).getStatus();
                rows[j][4] = Integer.toString(RentalOrderDirectory.getInstance().getRentalOrdersDir().get(i).getItemId());
                rows[j][5] = RentalOrderDirectory.getInstance().getRentalOrdersDir().get(i).getOrderedBy();
                rows[j][6] = RentalOrderDirectory.getInstance().getRentalOrdersDir().get(i).getOrderID();
                j++;
            }
        }
        
        DefaultTableModel dtm = new DefaultTableModel (rows, columnNames);
        vio.tblOpReq.setModel(dtm);
        vio.show();
    }//GEN-LAST:event_btnServiceActionPerformed

